"""A simple class to hold properties

Every event, band, and venue will have the following properties
"""


class EventProperties:

    DATE = 'event_date'
    TIME = 'event_time'
    PRICE = 'event_price'
    DATE_STATUS = 'event_date_status'


class ArtistProperties:

    ARTIST_NAME = 'artist_name'
    FACEBOOK_URL = 'facebook_url'


class VenueProperties:

    NAME = 'venue_name'
    WEBSITE_URL = 'venue_url'
    ADDRESS = 'venue_address'
